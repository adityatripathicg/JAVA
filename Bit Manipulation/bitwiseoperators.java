public class bitwiseoperators{
    public static void main(String args[]){

        //Binary AND
        System.out.println(5&6);
        //BInary OR
        System.out.println(5|6);
        //XOR Operators
        System.out.println(5^6);
        //One's Complement
        System.out.println(~5);
        //Binary Left Shift
        System.out.println(5<<2);
        //Binary Right Shft
        System.out.println(6>>1);

    }
}